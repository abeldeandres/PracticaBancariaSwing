        private void loadData() {
        
        DefaultTableModel tm = (DefaultTableModel)tblArticulos.getModel();
        
        Iterator<Producto> it = this.cliente.getListaProductos().iterator();
        while (it.hasNext()) {
            Object[] rowData = new Object[1];
            rowData[0] = it.next();
            tm.addRow(rowData);
        }
    }

